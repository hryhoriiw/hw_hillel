// Даны два различных числа. Определить какое из них больше, а какое меньше.

const digitFirst = parseFloat(prompt('take me first digit'));
const digitSecond = parseFloat(prompt('take me second digit'));

if (digitFirst>digitSecond) {
    alert('your first digit > second')
} else if (digitFirst<digitSecond) {
    alert('your second digit > first')
} else {
    alert ('your first digit = second')
}





















